/**
 * 获取配方
 * @param recipeId 配方ID
 * @returns {Recipe} 配方信息
 * @throws {Error} 配方不存在
 */
export function getRecipeById(recipeId: Id): Recipe {
  const recipe = recipes.find(({ id }) => id === recipeId)
  if (!recipe) {
    throw new Error(`配方不存在: ${recipeId}`)
  }
  return recipe
}
